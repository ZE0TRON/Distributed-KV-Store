package de.tum.i13.server.storageManagment;

/**
 * {@code CacheDisplacementStrategy} is an enum representing the 3 cache
 * displacement strategies - FIFO (First in First Out), LRU (Least Recently
 * Used), LFU (Least Frequently Used).
 */
public enum CacheDisplacementStrategy {
	FIFO, LRU, LFU;

}
